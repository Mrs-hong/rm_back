#!/bin/bash
# =============================================================================
# 公共函数库 - database/common.sh
# =============================================================================
# 说明：
#   本文件为 MariaDB 和 openGauss 数据库操作脚本的公共函数库。
#   各脚本通过 source 本文件引入公共能力。
#   所有函数和变量均带 db_ 前缀，避免命名冲突。
# =============================================================================

# -----------------------------------------------------------------------------
# 返回值定义（供 C++ 程序解析）
# -----------------------------------------------------------------------------
readonly DB_EXIT_SUCCESS=0           # 成功
readonly DB_EXIT_GENERIC_ERROR=1     # 通用错误
readonly DB_EXIT_INVALID_ARGS=2      # 参数错误或缺失
readonly DB_EXIT_PERMISSION_DENIED=3 # 权限不足（非 root）
readonly DB_EXIT_MISSING_DEPEND=4    # 依赖缺失/安装包不存在
readonly DB_EXIT_SERVICE_STATE=5     # 服务已运行/未运行（状态冲突）
readonly DB_EXIT_PORT_IN_USE=6       # 端口被占用
readonly DB_EXIT_INIT_FAILED=7       # 初始化失败
readonly DB_EXIT_DB_CONNECT_FAILED=8 # 数据库连接失败

# -----------------------------------------------------------------------------
# 日志输出函数
# -----------------------------------------------------------------------------
db_log_info() {
    echo "[INFO] $(date '+%Y-%m-%d %H:%M:%S') $*"
}

db_log_error() {
    echo "[ERROR] $(date '+%Y-%m-%d %H:%M:%S') $*" >&2
}

db_log_warn() {
    echo "[WARN] $(date '+%Y-%m-%d %H:%M:%S') $*" >&2
}

# -----------------------------------------------------------------------------
# 退出辅助函数：打印错误并返回指定退出码
# -----------------------------------------------------------------------------
db_exit_with_error() {
    local code=$1
    shift
    db_log_error "$*"
    exit "$code"
}

# -----------------------------------------------------------------------------
# 检查是否以 root 权限运行
# -----------------------------------------------------------------------------
db_check_root() {
    if [[ $EUID -ne 0 ]]; then
        db_exit_with_error $DB_EXIT_PERMISSION_DENIED "需要 root 权限运行此脚本，请使用 sudo"
    fi
}

# -----------------------------------------------------------------------------
# 检测系统架构
# 返回：x86_64 | aarch64 | arm64 | 其他
# -----------------------------------------------------------------------------
db_check_arch() {
    local arch
    arch=$(uname -m)
    case "$arch" in
        x86_64|amd64)
            echo "x86_64"
            ;;
        aarch64|arm64)
            echo "aarch64"
            ;;
        *)
            echo "$arch"
            ;;
    esac
}

# -----------------------------------------------------------------------------
# 检测操作系统类型（基于 /etc/os-release）
# 返回：ubuntu | debian | centos | rhel | unknown
# -----------------------------------------------------------------------------
db_check_os() {
    if [[ -f /etc/os-release ]]; then
        # shellcheck source=/dev/null
        source /etc/os-release
        echo "$ID" | tr '[:upper:]' '[:lower:]'
    else
        echo "unknown"
    fi
}

# -----------------------------------------------------------------------------
# 检查端口是否被占用
# 参数：$1=端口号
# 返回：0(占用) 或 1(未占用)
# -----------------------------------------------------------------------------
db_check_port_in_use() {
    local port=$1
    if command -v ss >/dev/null 2>&1; then
        ss -tuln | grep -qE ":${port}([[:space:]]|$)"
    elif command -v netstat >/dev/null 2>&1; then
        netstat -tuln 2>/dev/null | grep -qE ":${port}([[:space:]]|$)"
    else
        (echo >/dev/tcp/127.0.0.1/"$port") >/dev/null 2>&1
    fi
}

# -----------------------------------------------------------------------------
# 通用参数解析辅助函数
# 将传入的参数解析为变量，支持 --key=value 和 --key value 两种形式
# 用法：
#   db_parse_args "$@"
#   之后可通过变量 args_key 获取值
#
# 示例：
#   --admin_password=123 -> 变量 db_arg_admin_password=123
#   --admin_password 123 -> 变量 db_arg_admin_password=123
# -----------------------------------------------------------------------------
declare -A db_args
db_parse_args() {
    unset db_args
    declare -gA db_args
    while [[ $# -gt 0 ]]; do
        local key="$1"
        if [[ "$key" == --* ]]; then
            key="${key#--}"
            if [[ "$key" == *=* ]]; then
                local val="${key#*=}"
                key="${key%%=*}"
                db_args["$key"]="$val"
            else
                if [[ $# -gt 1 && "$2" != --* ]]; then
                    db_args["$key"]="$2"
                    shift
                else
                    db_args["$key"]="1"
                fi
            fi
        fi
        shift
    done
}

# -----------------------------------------------------------------------------
# 获取解析后的参数值
# -----------------------------------------------------------------------------
db_arg() {
    local key="$1"
    local default="${2:-}"
    if [[ -v "db_args[$key]" ]]; then
        echo "${db_args[$key]}"
    else
        echo "$default"
    fi
}

# -----------------------------------------------------------------------------
# 打印帮助信息辅助函数
# 参数：$1=脚本名称, $2=描述, $3=参数列表字符串
# -----------------------------------------------------------------------------
db_print_help() {
    local script_name="$1"
    local description="$2"
    local params="$3"
    cat <<EOF
用法: $script_name [选项]

说明:
  $description

选项:
$params

返回值:
  0 成功
  1 通用错误
  2 参数错误或缺失
  3 权限不足（非 root）
  4 依赖缺失/安装包不存在
  5 服务已运行/未运行（状态冲突）
  6 端口被占用
  7 初始化失败
  8 数据库连接失败
EOF
}

# -----------------------------------------------------------------------------
# 检查命令是否存在
# -----------------------------------------------------------------------------
db_command_exists() {
    command -v "$1" >/dev/null 2>&1
}

# -----------------------------------------------------------------------------
# 安全执行命令：失败时返回指定错误码而非直接退出
# 参数：$1=错误码, $2+=命令
# -----------------------------------------------------------------------------
db_run_or_return() {
    local code="$1"
    shift
    if ! "$@"; then
        return "$code"
    fi
}

# -----------------------------------------------------------------------------
# 检查数据目录是否包含有效的 MariaDB/MySQL 数据库数据
# 说明：通过检查 mysql/ 系统数据库目录和核心文件（ibdata1 或 aria_log_control）
#       避免被 debian-*.flag 等标记文件误导。
# 参数：$1=数据目录路径
# 返回：0 有有效数据，1 无有效数据
# -----------------------------------------------------------------------------
db_has_valid_data() {
    local dir="$1"
    [[ -d "$dir/mysql" ]] && [[ -f "$dir/ibdata1" || -f "$dir/aria_log_control" ]]
}

# -----------------------------------------------------------------------------
# 以指定用户执行命令
# 说明：如果当前用户就是目标用户，则直接执行；否则通过 su 切换。
# 参数：$1=目标用户名, $2=要执行的命令字符串
# 注意：命令字符串中若包含引号，需自行转义或使用单引号包裹整体。
# -----------------------------------------------------------------------------
db_run_as_user() {
    local target_user="$1"
    local cmd="$2"
    if [[ "$(whoami)" == "$target_user" ]]; then
        bash -c "$cmd"
    else
        su - "$target_user" -c "$cmd"
    fi
}

# -----------------------------------------------------------------------------
# SQL 字符串转义
# 说明：将单引号替换为 ''，反斜杠替换为 \\，用于 SQL 单引号字符串内安全拼接
# 参数：$1=原始字符串
# 返回：转义后的字符串（stdout）
# -----------------------------------------------------------------------------
db_sql_escape() {
    local str="$1"
    str="${str//\\/\\\\}"
    str="${str//\'/\'\'}"
    echo "$str"
}

# -----------------------------------------------------------------------------
# 路径安全校验
# 说明：校验路径是否安全可用于 rm -rf 操作，拒绝根目录、空路径、过浅路径等
# 参数：$1=路径
# 返回：0 安全，1 不安全
# -----------------------------------------------------------------------------
db_validate_path() {
    local path="$1"
    [[ -z "$path" ]] && return 1
    [[ "$path" == "/" ]] && return 1
    case "$path" in *..*) return 1 ;; esac
    local depth
    depth=$(echo "$path" | tr -cd '/' | wc -c)
    [[ $depth -lt 2 ]] && return 1
    return 0
}

# -----------------------------------------------------------------------------
# 安全删除目录
# 说明：先校验路径安全性，再执行 rm -rf
# 参数：$1=路径, $2=描述（可选，用于日志）
# 返回：0 成功，1 路径不安全
# -----------------------------------------------------------------------------
db_safe_rm() {
    local path="$1"
    local desc="${2:-目录}"
    if ! db_validate_path "$path"; then
        db_log_error "拒绝删除不安全路径: $path"
        return 1
    fi
    db_log_info "删除${desc}: $path"
    rm -rf "$path"
}

# -----------------------------------------------------------------------------
# 等待 MySQL/MariaDB 服务就绪
# 说明：通过 mysql -u root 尝试连接，循环重试直到成功或超时
# 参数：$1=最大重试次数（可选，默认 30）, $2=重试间隔秒数（可选，默认 1）
# 返回：0 就绪，1 超时
# -----------------------------------------------------------------------------
db_wait_for_mysql() {
    local max_attempts="${1:-30}"
    local interval="${2:-1}"
    local attempt=1
    while [[ $attempt -le $max_attempts ]]; do
        if mysql -u root -e "SELECT 1;" >/dev/null 2>&1; then
            return 0
        fi
        sleep "$interval"
        ((attempt++))
    done
    return 1
}

# -----------------------------------------------------------------------------
# 创建 MySQL 临时配置文件（安全传递密码，避免命令行暴露）
# 说明：生成权限为 600 的临时 [client] 配置文件，调用方负责使用后清理
# 参数：$1=user, $2=password, $3=host, $4=port（均可选，为空则不写入）
# 返回：配置文件路径（stdout）
# -----------------------------------------------------------------------------
db_create_mysql_cnf() {
    local user="$1" password="$2" host="$3" port="$4"
    local cnf_file
    cnf_file=$(mktemp /tmp/.mysql.cnf.XXXXXX)
    chmod 600 "$cnf_file"
    {
        echo "[client]"
        [[ -n "$user" ]] && echo "user=$user"
        [[ -n "$password" ]] && echo "password=$password"
        [[ -n "$host" ]] && echo "host=$host"
        [[ -n "$port" ]] && echo "port=$port"
    } >> "$cnf_file"
    echo "$cnf_file"
}
